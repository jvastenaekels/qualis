def _generate_code(language: str) -> str:
    """Generate a single adjective-noun-NNN code for *language*."""
    words = WORD_LISTS.get(language) or WORD_LISTS["en"]
    adj = secrets.choice(words["adjectives"])
    noun = secrets.choice(words["nouns"])
    number = 100 + secrets.randbelow(900)  # 100..999
    return f"{adj}-{noun}-{number}"


async def generate_unique_resume_code(
    db: AsyncSession,
    language: str,
    *,
    max_attempts: int = 5,
) -> str:
    """Generate a memorable resume code that is unique in the DB.

    Tries up to *max_attempts* random codes.  If all collide (extremely
    unlikely with ~9M combinations), falls back to a longer code by
    appending extra digits.
    """
    for _ in range(max_attempts):
        code = _generate_code(language)
        exists = await db.execute(
            select(Participant.id).where(Participant.resume_code == code).limit(1)
        )
        if exists.scalar_one_or_none() is None:
            return code

    # Fallback: extend with extra digits to reduce collision probability.
    # The caller's savepoint-retry loop provides the final safety net.
    code = _generate_code(language)
    extra = secrets.randbelow(9000) + 1000  # 1000..9999
    fallback = f"{code}-{extra}"
    exists = await db.execute(
        select(Participant.id).where(Participant.resume_code == fallback).limit(1)
    )
    if exists.scalar_one_or_none() is None:
        return fallback
    # Last resort: append more entropy
    return f"{fallback}-{secrets.randbelow(9000) + 1000}"
